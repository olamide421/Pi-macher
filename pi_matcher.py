def find_col(df, candidates):
    for c in candidates:
        if c in df.columns:
            return c
    # fuzzy fallback
    lcols = {c.lower(): c for c in df.columns}
    for cand in candidates:
        for cl,orig in lcols.items():
            if cand in cl:
                return orig
    return None

